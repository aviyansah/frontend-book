// The time that the notification will be shown for (in milliseconds). Default is 2500
humane.timeout

// Whether the notification will wait for user's interaction (such as hovering over
// the notification or tapping) before disapperaing. Default is false
humane.waitForMove

// Whether to allow the user to hide the notification by clicking on it. Default is false
humane.clickToClose

// The delay in milliseconds that is activated after user hovers over the notification.
// Should be used together with waitForMove. Default is 0
humane.timeoutAfterMove

// The CSS class that will be added to all notifications beside the base class.
// Default is empty.
humane.addnCls

// The base CSS class that notifications will use. Default is "humane"
humane.baseCls
